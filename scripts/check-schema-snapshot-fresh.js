/**
 * check-schema-snapshot-fresh.js
 * ---------------------------------------------------------------------------
 * Fails if supabase/schema/ still describes something a migration removed.
 *
 * Why this exists
 * ---------------
 * v3.74.759 dropped three SECURITY DEFINER functions that rewrote cost of goods
 * sold across every company and were callable without logging in. The database
 * was fixed the same hour. The checked-in snapshot was not — and it still held:
 *
 *     GRANT EXECUTE ON FUNCTION public.fix_all_historical_cogs() TO anon;
 *     GRANT EXECUTE ON FUNCTION public.fix_cogs_clean() TO anon;
 *     GRANT EXECUTE ON FUNCTION public.recalculate_cogs() TO anon;
 *
 * A rebuild from the repository would have restored the functions AND the anon
 * grants: precisely what the release removed, reinstated by the file whose job
 * is to be the safe copy. Nothing would have reported it. The snapshot is only
 * regenerated by hand, so it drifts silently by default, and a stale mirror is
 * more dangerous than no mirror because it is trusted.
 *
 * This is the same failure shape as the rest of the work this week: something
 * that reports success while doing nothing, or in this case, says nothing at all
 * while quietly disagreeing with reality.
 *
 * What it checks
 * --------------
 * Offline and generic — no database connection, so it runs in CI:
 *
 *   1. Every `DROP FUNCTION ... public.<name>(` in supabase/migrations must not
 *      have a matching `CREATE OR REPLACE FUNCTION public.<name>(` left in
 *      functions.sql, unless a LATER migration recreates it.
 *   2. No dropped function may still carry grants in schema.sql.
 *
 * It cannot detect a function ADDED to the database and missing from the
 * snapshot — that needs a live connection. It catches the direction that
 * matters, where the snapshot would resurrect something deliberately removed.
 *
 * Run:   node scripts/check-schema-snapshot-fresh.js
 * CI:    non-zero exit fails the build.
 * ---------------------------------------------------------------------------
 */
const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const MIGRATIONS = path.join(ROOT, "supabase", "migrations");
const FUNCTIONS_SQL = path.join(ROOT, "supabase", "schema", "functions.sql");
const SCHEMA_SQL = path.join(ROOT, "supabase", "schema", "schema.sql");

const DROP_RE = /DROP\s+FUNCTION\s+(?:IF\s+EXISTS\s+)?public\.(\w+)\s*\(/gi;
const CREATE_RE = /CREATE\s+(?:OR\s+REPLACE\s+)?FUNCTION\s+public\.(\w+)\s*\(/gi;

/**
 * A name is not an identity. Postgres allows overloads, and the single most
 * common migration in this repository drops one overload and creates another:
 *
 *     DROP FUNCTION public.create_notification(uuid, text);
 *     CREATE OR REPLACE FUNCTION public.create_notification(uuid, text, text)
 *
 * Matching on the name alone calls that a deletion and then reports the live,
 * correct function as stale snapshot debris. So every comparison here is
 * name + arity, never name.
 *
 * Arity is counted at paren depth 1 only, so composite defaults and types like
 * numeric(12,2) or jsonb[] do not inflate the count.
 */
function arityFrom(src, openParenIndex) {
  let depth = 0, commas = 0, seenNonSpace = false;
  for (let i = openParenIndex; i < src.length; i++) {
    const c = src[i];
    if (c === "(") { depth++; continue; }
    if (c === ")") { depth--; if (depth === 0) return seenNonSpace ? commas + 1 : 0; continue; }
    if (depth === 1) {
      if (c === ",") commas++;
      if (!/\s/.test(c)) seenNonSpace = true;
    }
  }
  return null; // unbalanced — caller treats as unknown
}

const key = (name, arity) => `${name}/${arity}`;

/**
 * Self-test. Every guard written this week that matched a bare NAME ended up
 * rejecting its own documentation — this file's own comment block names all
 * three dropped functions, so a name-matching rule would fail on itself.
 * Both directions are pinned.
 */
const FIXTURES = [
  ["DROP FUNCTION IF EXISTS public.foo();", "drop", "foo"],
  ["DROP FUNCTION public.bar(uuid);", "drop", "bar"],
  ["drop function if exists public.baz();", "drop", "baz"],
  ["-- we dropped public.qux() last week", "drop", null],
  ["CREATE OR REPLACE FUNCTION public.foo()", "create", "foo"],
  ["CREATE FUNCTION public.bar(p_id uuid)", "create", "bar"],
  [" * CREATE OR REPLACE FUNCTION public.notreal()", "create", "notreal"],
];

const firstMatch = (re, line) => {
  re.lastIndex = 0;
  const m = re.exec(line);
  return m ? m[1] : null;
};

const fixtureFailures = FIXTURES.filter(([line, kind, expected]) => {
  const got = firstMatch(kind === "drop" ? DROP_RE : CREATE_RE, line);
  return got !== expected;
});
if (fixtureFailures.length > 0) {
  console.error("X The rule itself is broken - self-test fixtures failed:\n");
  for (const [line, kind, expected] of fixtureFailures) {
    const got = firstMatch(kind === "drop" ? DROP_RE : CREATE_RE, line);
    console.error(`   ${kind}: expected ${expected}, got ${got}`);
    console.error(`      ${line.trim()}\n`);
  }
  process.exit(1);
}

if (!fs.existsSync(FUNCTIONS_SQL) || !fs.existsSync(SCHEMA_SQL)) {
  console.log("! No schema snapshot on disk - nothing to compare. Skipping.");
  process.exit(0);
}

// Migrations sort chronologically by filename, so a later CREATE legitimately
// undoes an earlier DROP. Track the last verb applied to each function name.
const files = fs.existsSync(MIGRATIONS)
  ? fs.readdirSync(MIGRATIONS).filter((f) => f.endsWith(".sql")).sort()
  : [];

const lastVerb = new Map(); // name -> { verb, file }

for (const file of files) {
  const src = fs.readFileSync(path.join(MIGRATIONS, file), "utf8");
  // Strip line comments so prose describing a drop is not read as one. The
  // migrations for v3.74.759 discuss all three dropped functions at length.
  const code = src.replace(/^\s*--.*$/gm, "");

  // ORDER WITHIN THE FILE MATTERS, and getting this wrong is not theoretical:
  // the first version of this script applied every CREATE in a file and then
  // every DROP, which is not what the file does. The overwhelmingly common
  // migration shape is
  //
  //     DROP FUNCTION public.create_notification(uuid, text);   -- old overload
  //     CREATE OR REPLACE FUNCTION public.create_notification(uuid, text, text)
  //
  // and verb-order processing recorded that as a drop. It reported eight
  // functions as deleted-but-still-in-the-snapshot. All eight were live in the
  // database; every one was a false alarm produced by the checker, not a defect
  // in the snapshot. Had they been believed, the fix would have been to delete
  // eight working functions from the mirror.
  //
  // Interleave by match position so the LAST statement in the file wins.
  const events = [];
  for (const m of code.matchAll(CREATE_RE)) {
    const a = arityFrom(code, code.indexOf("(", m.index + m[0].length - 1));
    if (a !== null) events.push({ at: m.index, name: m[1], arity: a, verb: "create" });
  }
  for (const m of code.matchAll(DROP_RE)) {
    const a = arityFrom(code, code.indexOf("(", m.index + m[0].length - 1));
    if (a !== null) events.push({ at: m.index, name: m[1], arity: a, verb: "drop" });
  }
  events.sort((a, b) => a.at - b.at);
  for (const e of events) lastVerb.set(key(e.name, e.arity), { verb: e.verb, file, name: e.name, arity: e.arity });
}

const dropped = [...lastVerb.entries()].filter(([, v]) => v.verb === "drop");

const fnSrc = fs.readFileSync(FUNCTIONS_SQL, "utf8");
const scSrc = fs.readFileSync(SCHEMA_SQL, "utf8");

// Index every definition and grant in the snapshot by name + arity, so a
// surviving overload never makes a dropped one look present.
const snapshotDefs = new Set();
for (const m of fnSrc.matchAll(CREATE_RE)) {
  const a = arityFrom(fnSrc, fnSrc.indexOf("(", m.index + m[0].length - 1));
  if (a !== null) snapshotDefs.add(key(m[1], a));
}
const grantsByKey = new Map();
const GRANT_LINE_RE = /FUNCTION\s+public\.(\w+)\s*\(/i;
for (const line of scSrc.split("\n")) {
  const m = GRANT_LINE_RE.exec(line);
  if (!m) continue;
  const a = arityFrom(line, line.indexOf("(", m.index + m[0].length - 1));
  if (a === null) continue;
  const k = key(m[1], a);
  if (!grantsByKey.has(k)) grantsByKey.set(k, []);
  grantsByKey.get(k).push(line);
}

/**
 * The ceiling on this check, stated plainly.
 *
 * Only 49 of 661 applied migration versions correspond to a file in
 * supabase/migrations — the rest were applied through the SQL editor or MCP,
 * which records a timestamp and not a filename. So a function can be dropped by
 * a FILE and recreated by a migration this script cannot see, and it will look
 * stale when it is live and correct.
 *
 * Each entry below was checked against the live database by hand before being
 * added, and the evidence is recorded with it. This is a ratchet, not a mute
 * button: an unexplained entry here is worse than a failing check.
 */
const VERIFIED_LIVE = new Map([
  ["create_product_atomic/16",
   "Dropped by 20260422000200 and recreated outside the migrations folder. " +
   "Live 2026-07-20: two overloads present, 16 args (p_tax_code_id uuid) and " +
   "17 args (p_tax_code_id text, + p_product_type). The snapshot is right."],
]);

const stale = [];
for (const [k, { file, name, arity }] of dropped) {
  if (VERIFIED_LIVE.has(k)) continue;
  const inFunctions = snapshotDefs.has(k);
  const grantLines = grantsByKey.get(k) || [];
  if (inFunctions || grantLines.length > 0) {
    stale.push({ name, arity, file, inFunctions, grants: grantLines.length,
                 anonGrants: grantLines.filter((l) => /TO anon;/.test(l)).length });
  }
}

console.log(`Checked ${files.length} migration file(s) against the schema snapshot.`);
console.log(`${dropped.length} function(s) dropped and not recreated.`);

if (stale.length > 0) {
  console.error(`\nX ${stale.length} dropped function(s) still described by the snapshot:\n`);
  for (const s of stale) {
    console.error(`   ${s.name}/${s.arity} arg(s)  (dropped in ${s.file})`);
    if (s.inFunctions) console.error(`      still defined in supabase/schema/functions.sql`);
    if (s.grants > 0) {
      console.error(`      still granted in supabase/schema/schema.sql (${s.grants} line(s)` +
                    (s.anonGrants > 0 ? `, ${s.anonGrants} of them TO anon` : "") + `)`);
    }
  }
  console.error("\nRebuilding from this repository would recreate them, grants and all.");
  console.error("Regenerate the snapshot:");
  console.error("   node scripts/dump-db-functions.js");
  console.error("   node scripts/dump-db-schema.js");
  process.exit(1);
}

if (VERIFIED_LIVE.size > 0) {
  console.log(`${VERIFIED_LIVE.size} known exception(s), each verified live and justified in this file.`);
}
console.log("\n+ The snapshot describes nothing that a migration removed.");
