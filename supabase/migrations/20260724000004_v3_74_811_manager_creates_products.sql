-- ============================================================================
-- v3.74.811 — مدير الفرع يضيف منتجات (محاذاة طبقتى الصلاحيات)
-- ============================================================================
-- المالك رصد حياً: إضافة المنتج تنجح للمالك وتفشل 500 لمدير الفرع.
--
-- طبقتان متناقضتان:
--   * قائمة السماح فى مسار الإنشاء (v3.74.675، بمبدأ المالك): «الإدارة
--     أو مدير الفرع أو مسؤول المشتريات».
--   * جدول company_role_permissions: manager/products بـ can_write=false
--     — فحارس apiGuard يرفضه قبل بلوغ القائمة أصلاً.
--
-- (والانفجار 500 بدل رسالة 403 نظيفة سببه عيب منفصل فى apiGuard —
--  كان يعيد كائن خطأ خاماً بدل Response — أُصلح فى نفس الإصدار فى
--  lib/core/security/api-guard.ts).
--
-- هذا الملف يحاذى البيانات: منح manager الكتابة والتعديل على products
-- فى كل الشركات. الحذف يبقى للإدارة العليا.
-- ============================================================================

UPDATE company_role_permissions
SET can_write = TRUE, can_update = TRUE
WHERE resource = 'products' AND role = 'manager'
  AND (can_write = FALSE OR can_update = FALSE);
